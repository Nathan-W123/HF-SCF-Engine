# ── 6-31G* (= 6-31G + d polarization on heavy atoms) ─────────────────────────
# d exponents from Hariharan & Pople (1973), Table II
# H is unchanged from 6-31G
_D_POL = {
    "Li": 0.200, "Be": 0.400, "B": 0.600,
    "C":  0.800, "N":  0.800, "O": 0.800, "F": 0.800, "Ne": 0.800,
    "Na": 0.175, "Mg": 0.175, "Al": 0.325, "Si": 0.450,
    "P":  0.550, "S":  0.650, "Cl": 0.750, "Ar": 0.850,
}

def _make_6_31_gstar():
    d = {}
    for elem, shells in POPLE_6_31G.items():
        d[elem] = list(shells)
        if elem in _D_POL and elem != "H" and elem != "He":
            d[elem].append((2, [_D_POL[elem]], [1.0]))
    return d

POPLE_6_31G_STAR = _make_6_31_gstar()

# ── 6-31G** (= 6-31G* + p polarization on H) ─────────────────────────────────
def _make_6_31_gstarstar():
    d = {}
    for elem, shells in POPLE_6_31G_STAR.items():
        d[elem] = list(shells)
        if elem == "H":
            d[elem].append((1, [1.1000000], [1.0]))
    return d

POPLE_6_31G_STARSTAR = _make_6_31_gstarstar()

